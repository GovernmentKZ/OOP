public class DataVisit {

    int day;
    int month;
    int hours;
    int minute;
    String name;
    String nameClient;

    public DataVisit(int day, int month, int hours, int minute, String name, String nameClient) {
        this.day = day;
        this.month = month;
        this.hours = hours;
        this.minute = minute;
        this.name = name;
        this.nameClient = nameClient;
    }
}
